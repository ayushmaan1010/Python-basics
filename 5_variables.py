# Variables naming rules:

# snake_case (can't contain spaces)
# start with lowercase or underscore
# contains letters, numbers and underscore
# don't overwrite keywords
# case sensitive

# Convention: All caps variables are treated as constants.

user_iq = 190
user_age = user_iq/5

print(user_iq)
print(user_age)

# expression performs calculation, for eg: (user_iq/5)
# statements perform actions, for eg: (age = iq/5)
