# formatted strings

name = 'Johnny'
age = 55

# 'f' : tells python about the format of sentence after it.
print(f'Hi {name}. You are {age} years old.')

# we can also use .format() for formatting
print('Hi {}. You are {} years old.'.format(name, age))

